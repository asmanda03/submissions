def flip_case(phrase, to_swap):
    """Flip [to_swap] case each time it appears in phrase.

        >>> flip_case('Aaaahhh', 'a')
        'aAAAhhh'

        >>> flip_case('Aaaahhh', 'A')
        'aAAAhhh'

        >>> flip_case('Aaaahhh', 'h')
        'AaaaHHH'

    """ 
    swapped_phrase = ''
    lst = list(phrase)
    count = 0

    if to_swap.islower():
        for char in lst:
            if char.lower() == to_swap:
                lst[count] = char.swapcase()
            count += 1

    if to_swap.isupper():
        for char in lst:
            if char.upper() == to_swap:
                lst[count] = char.swapcase()
            count += 1
    
    for char in lst:
        swapped_phrase += char
    return swapped_phrase
